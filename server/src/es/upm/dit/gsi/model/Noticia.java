package es.upm.dit.gsi.model;

public class Noticia {
	public String id;
	public String nombre;
	public String video;
	public String captura;
	public String fecha;
	public String contenido;
	public String autor;
	public String have;
	public String estimacion;
	public String social;
	
	public Noticia() {
	}
	
	public Noticia(String id, String nombre, String video, String captura, String fecha, String contenido, 
			String autor, String have, String estimacion) {
		this.id = id;
		this.nombre = nombre;
		this.video = video;
		this.captura = captura;
		this.fecha = fecha;
		this.contenido = contenido;
		this.autor = autor;
		this.have = have;
		this.estimacion = estimacion;
	}

}











